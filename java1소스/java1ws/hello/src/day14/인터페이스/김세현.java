public class 김세현 implements 나는게가능한, 불어가능한, 변신가능한{

	@Override
	public void 변신하기() {
		System.out.println("김세현: 동물로 변신한다");
	}

	@Override
	public void 불어를한다() {
		System.out.println("김세현: 유창한 불어를 구사한다");
	}

	@Override
	public void 날수있다() {
		System.out.println("김세현: 허공을 떠서 날아다닌다");
	}

 

}
